    #[test]
    fn version_line_extraction_requires_tool_specific_output() {
        assert_eq!(
            extract_version_line(
                TOOL_NGSPICE,
                "******\n** ngspice-44 : Circuit level simulation program\n",
                ""
            ),
            Some("** ngspice-44 : Circuit level simulation program".to_string())
        );
        assert_eq!(
            extract_version_line(TOOL_KICAD, "8.0.4\n", ""),
            Some("8.0.4".to_string())
        );
        assert_eq!(
            extract_version_line(TOOL_NGSPICE, "unrelated tool 44", ""),
            None
        );
        assert_eq!(
            extract_version_line(TOOL_NGSPICE, "fake wrapper ngspice-44", ""),
            None
        );
        assert_eq!(extract_version_line(TOOL_KICAD, "Python 3.12.2", ""), None);
        assert_eq!(extract_version_line(TOOL_NGSPICE, "44", ""), None);
    }

    #[test]
    fn version_probe_rejects_nonzero_exit_and_unrelated_executables_with_digits() {
        let failed = probe_outcome(
            Some(1),
            "** ngspice-44 : Circuit level simulation program",
            "",
        );
        assert_eq!(
            version_from_probe(Path::new("ngspice.exe"), TOOL_NGSPICE, &failed),
            None
        );

        let digits = probe_outcome(Some(0), "8.0.4", "");
        assert_eq!(
            version_from_probe(Path::new("python.exe"), TOOL_KICAD, &digits),
            None
        );
        assert_eq!(
            version_from_probe(Path::new("kicad-cli.exe"), TOOL_KICAD, &digits),
            Some("8.0.4".into())
        );

        let ngspice = probe_outcome(
            Some(0),
            "",
            "** ngspice-46 : Circuit level simulation program",
        );
        assert_eq!(
            version_from_probe(Path::new("ngspice_con.exe"), TOOL_NGSPICE, &ngspice),
            Some("** ngspice-46 : Circuit level simulation program".into())
        );
    }
