    #[test]
    fn supported_executable_names_cover_console_and_cross_platform_variants() {
        for program in [
            "ngspice",
            "ngspice.exe",
            "ngspice_con",
            "ngspice_con.exe",
            "/usr/local/bin/ngspice",
            "C:/Spice64/bin/ngspice_con.exe",
        ] {
            assert!(supported_executable_name(TOOL_NGSPICE, Path::new(program)));
        }
        for program in [
            "kicad-cli",
            "kicad-cli.exe",
            "/Applications/KiCad/KiCad.app/Contents/MacOS/kicad-cli",
            "C:/Program Files/KiCad/9.0/bin/kicad-cli.exe",
        ] {
            assert!(supported_executable_name(TOOL_KICAD, Path::new(program)));
        }
        assert!(!supported_executable_name(
            TOOL_KICAD,
            Path::new("python.exe")
        ));
        assert!(!supported_executable_name(
            TOOL_NGSPICE,
            Path::new("ngspice-helper.exe")
        ));
    }

